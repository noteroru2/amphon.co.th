function baseFrontmatter(item) {
  return {
    title: item.title,
    slug: item.slug,
    description: item.description,
    mainKeyword: item.mainKeyword,
    relatedKeywords: item.relatedKeywords,
    heroImage: item.heroImage,
    ogImage: item.ogImage,
    faqs: item.faqs,
    date: DATE,
    updated: UPDATED,
    draft: false,
  };
}

let written = 0;

for (const s of services) {
  const { file, body, icon, ...rest } = s;
  writeMd('services', file, { ...baseFrontmatter(rest), icon }, body);
  written++;
}

for (const a of areas) {
  let body;
  if (a.slug === 'อุบลราชธานี') body = ubonBody(a);
  else if (a.slug === KK) body = khonkaenBody(a);
  else body = areaBodyFromEntry(a);
  writeMd('areas', `${a.slug}.md`, { ...baseFrontmatter(a), province: a.province }, body);
  written++;
}

for (const sa of serviceAreas) {
  const { file, body, serviceSlug, areaSlug, ...rest } = sa;
  writeMd('serviceAreas', file, { ...baseFrontmatter(rest), serviceSlug, areaSlug }, body);
  written++;
}

for (const b of blog) {
  const { file, body, ...rest } = b;
  writeMd('blog', file, baseFrontmatter(rest), body);
  written++;
}

console.log(`Seeded ${written} production markdown files.`);
