function writeMd(subdir, filename, frontmatter, body) {
  const dir = path.join(contentDir, subdir);
  fs.mkdirSync(dir, { recursive: true });
  const fm = Object.entries(frontmatter)
    .map(([k, v]) => {
      if (Array.isArray(v)) {
        if (v.length === 0) return `${k}: []`;
        if (typeof v[0] === 'object') {
          const items = v
            .map(
              (item) =>
                `  - question: ${item.question}\n    answer: ${JSON.stringify(item.answer).slice(1, -1)}`,
            )
            .join('\n');
          return `${k}:\n${items}`;
        }
        return `${k}:\n${v.map((i) => `  - ${i}`).join('\n')}`;
      }
      if (typeof v === 'boolean') return `${k}: ${v}`;
      if (typeof v === 'string' && (v.includes(':') || v.includes('#'))) return `${k}: "${v}"`;
      return `${k}: ${v}`;
    })
    .join('\n');
  fs.writeFileSync(path.join(dir, filename), `---\n${fm}\n---\n\n${body.trim()}\n`, 'utf8');
}

