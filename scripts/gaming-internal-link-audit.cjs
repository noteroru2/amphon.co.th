const SECTION_HEADERS = [
  '## บริการรับซื้อที่เกี่ยวข้อง',
  '## Related internal links',
  '## บริการอื่นๆ ที่เกี่ยวข้องจากเครือข่ายของเรา (Amphon Network)',
];

function renderSection(links) {
  const lines = links.map(([text, href]) => `- [${text}](${href})`);
  return `## บริการรับซื้อที่เกี่ยวข้อง\n\n${lines.join('\n')}`;
}

function stripOldSections(body) {
  let out = body;
  for (const header of SECTION_HEADERS) {
    const re = new RegExp(
      `${header.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[\\s\\S]*?(?=\\n## |$)`,
      'm',
    );
    out = out.replace(re, '');
  }
  // Fix broken rog-gaming-notebook hrefs site-wide in remaining body
  out = out.replace(/\/บริการ\/รับซื้อ-rog-gaming-notebook/g, u(ROG_HUB));
  return out.replace(/\n{3,}/g, '\n\n').trimEnd();
}

function getSlugFromFile(filePath) {
  const content = fs.readFileSync(filePath, 'utf8');
  const m = content.match(/^slug:\s*"?([^"\n]+)"?/m);
  return m ? m[1] : null;
}

function findFileBySlug(slug) {
  for (const f of fs.readdirSync(SERVICES)) {
    if (!f.endsWith('.md')) continue;
    const fp = path.join(SERVICES, f);
    if (getSlugFromFile(fp) === slug) return fp;
  }
  return null;
}

function insertSection(body, section) {
  const cleaned = stripOldSections(body);
  const finalCtaIdx = cleaned.search(/^## Final CTA/m);
  if (finalCtaIdx >= 0) {
    return `${cleaned.slice(0, finalCtaIdx).trimEnd()}\n\n${section}\n\n${cleaned.slice(finalCtaIdx)}`;
  }
  const ctaIdx = cleaned.search(/^<div style="text-align: center/m);
  if (ctaIdx >= 0) {
    return `${cleaned.slice(0, ctaIdx).trimEnd()}\n\n${section}\n\n${cleaned.slice(ctaIdx)}`;
  }
  return `${cleaned.trimEnd()}\n\n${section}\n`;
}

let updated = 0;
for (const [slug, links] of Object.entries(LINK_BLOCKS)) {
  const fp = findFileBySlug(slug);
  if (!fp) {
    console.warn('MISSING FILE for slug:', slug);
    continue;
  }
  const content = fs.readFileSync(fp, 'utf8');
  const fmEnd = content.indexOf('---', 4) + 4;
  const frontmatter = content.slice(0, fmEnd);
  const body = content.slice(fmEnd);
  const section = renderSection(links);
  const newBody = insertSection(body, section);
  fs.writeFileSync(fp, frontmatter + newBody, 'utf8');
  console.log('OK', slug);
  updated++;
}

